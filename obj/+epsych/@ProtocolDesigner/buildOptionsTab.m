function buildOptionsTab(obj)
    optionsPanel = uipanel(obj.OptionsTab, ...
        'Position', [22 34 1156 574], ...
        'Title', 'Protocol Options', ...
        'FontWeight', 'bold', ...
        'BackgroundColor', [0.976 0.981 0.989], ...
        'ForegroundColor', [0.24 0.32 0.42]);

    heroPanel = uipanel(obj.OptionsTab, ...
        'Parent', optionsPanel, ...
        'Position', [18 434 1120 108], ...
        'Title', 'Execution Settings', ...
        'FontWeight', 'bold', ...
        'BackgroundColor', [0.985 0.989 0.995], ...
        'ForegroundColor', [0.24 0.32 0.42]);

    uilabel(heroPanel, 'Text', 'Trial Function:', 'Position', [18 48 92 22], ...
        'FontWeight', 'bold');
    obj.EditTrialFunc = uieditfield(obj.OptionsTab, 'text', ...
        'Parent', heroPanel, ...
        'Position', [118 46 354 26], ...
        'BackgroundColor', [0.997 0.998 0.999], ...
        'ValueChangedFcn', @(~, ~) obj.onOptionControlChanged());

    uilabel(heroPanel, 'Text', 'ISI (ms):', 'Position', [18 14 72 22], ...
        'FontWeight', 'bold');
    obj.EditISI = uieditfield(obj.OptionsTab, 'numeric', ...
        'Parent', heroPanel, ...
        'Position', [118 12 112 26], ...
        'Limits', [0 Inf], ...
        'ValueChangedFcn', @(~, ~) obj.onOptionControlChanged());

    obj.CheckCompileAtRuntime = uicheckbox(obj.OptionsTab, ...
        'Parent', heroPanel, ...
        'Text', 'Compile At Runtime', ...
        'Position', [560 48 190 22], ...
        'FontWeight', 'bold', ...
        'ValueChangedFcn', @(~, ~) obj.onOptionControlChanged());

    obj.CheckIncludeWAVBuffers = uicheckbox(obj.OptionsTab, ...
        'Parent', heroPanel, ...
        'Text', 'Include WAV Buffers', ...
        'Position', [560 16 190 22], ...
        'FontWeight', 'bold', ...
        'ValueChangedFcn', @(~, ~) obj.onOptionControlChanged());

    uilabel(optionsPanel, ...
        'Text', 'Core timing and runtime settings for compiled protocols.', ...
        'Position', [24 548 420 18], ...
        'FontAngle', 'italic', ...
        'FontColor', [0.38 0.45 0.55]);

    uilabel(optionsPanel, ...
        'Text', 'Use the Preview tab to compile and inspect the resulting trial matrix.', ...
        'Position', [24 406 430 18], ...
        'FontColor', [0.35 0.42 0.52]);
end

