function buildOptionsTab(obj, parent)
    if nargin < 2 || isempty(parent)
        parent = obj.Figure;
    end

    optionsPanel = uipanel(parent, ...
        'Position', [20 20 760 360], ...
        'Title', 'Protocol Options', ...
        'FontWeight', 'bold', ...
        'BackgroundColor', [0.976 0.981 0.989], ...
        'ForegroundColor', [0.24 0.32 0.42]);

    heroPanel = uipanel(parent, ...
        'Parent', optionsPanel, ...
        'Position', [18 206 724 118], ...
        'Title', 'Execution Settings', ...
        'FontWeight', 'bold', ...
        'BackgroundColor', [0.985 0.989 0.995], ...
        'ForegroundColor', [0.24 0.32 0.42]);

    uilabel(heroPanel, 'Text', 'Trial Function:', 'Position', [18 48 92 22], ...
        'FontWeight', 'bold');
    obj.EditTrialFunc = uieditfield(parent, 'text', ...
        'Parent', heroPanel, ...
        'Position', [118 46 420 26], ...
        'BackgroundColor', [0.997 0.998 0.999], ...
        'ValueChangedFcn', @(~, ~) obj.onOptionControlChanged());

    obj.CheckCompileAtRuntime = uicheckbox(parent, ...
        'Parent', heroPanel, ...
        'Text', 'Compile At Runtime', ...
        'Position', [560 48 148 22], ...
        'FontWeight', 'bold', ...
        'ValueChangedFcn', @(~, ~) obj.onOptionControlChanged());

    obj.CheckIncludeWAVBuffers = uicheckbox(parent, ...
        'Parent', heroPanel, ...
        'Text', 'Include WAV Buffers', ...
        'Position', [560 16 160 22], ...
        'FontWeight', 'bold', ...
        'ValueChangedFcn', @(~, ~) obj.onOptionControlChanged());

    uilabel(optionsPanel, ...
        'Text', 'Core timing and runtime settings for compiled protocols.', ...
        'Position', [24 330 420 18], ...
        'FontAngle', 'italic', ...
        'FontColor', [0.38 0.45 0.55]);

    uilabel(optionsPanel, ...
        'Text', 'Use the Preview tab to compile and inspect the resulting trial matrix.', ...
        'Position', [24 180 430 18], ...
        'FontColor', [0.35 0.42 0.52]);
end

