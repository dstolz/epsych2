function buildPreviewTab(obj, parent)
    if nargin < 2 || isempty(parent)
        parent = obj.Figure;
    end

    previewPanel = uipanel(parent, ...
        'Position', [20 20 1060 660], ...
        'Title', 'Compiled Trial Preview', ...
        'FontWeight', 'bold', ...
        'BackgroundColor', [0.976 0.981 0.989], ...
        'ForegroundColor', [0.24 0.32 0.42]);

    obj.BtnCompile = uibutton(parent, 'push', ...
        'Parent', previewPanel, ...
        'Text', 'Compile Protocol', ...
        'Position', [20 604 148 34], ...
        'FontWeight', 'bold', ...
        'ButtonPushedFcn', @(~, ~) obj.onCompile());

    obj.LabelCompileSummary = uilabel(parent, ...
        'Parent', previewPanel, ...
        'Text', 'Not compiled', ...
        'Position', [188 604 836 32], ...
        'FontWeight', 'bold', ...
        'FontColor', [0.22 0.28 0.36]);

    obj.TableCompiled = uitable(parent, ...
        'Parent', previewPanel, ...
        'Position', [20 20 1020 568], ...
        'ColumnEditable', false);
end

