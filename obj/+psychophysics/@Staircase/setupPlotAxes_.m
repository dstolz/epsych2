function setupPlotAxes_(obj)
% setupPlotAxes_(obj)
% Create and configure graphics objects for the staircase history plot.
%
% Parameters:
%   obj — psychophysics.Staircase instance

if isempty(obj.plotAxes_) || ~isvalid(obj.plotAxes_)
    return
end

grid(obj.plotAxes_, 'on');
box(obj.plotAxes_, 'on');
xlabel(obj.plotAxes_, 'Trial Index', 'Interpreter', 'none');
ylabel(obj.plotAxes_, char(obj.ParameterName), 'Interpreter', 'none');

hold(obj.plotAxes_,'on')

obj.h_thrreg = patch(obj.plotAxes_,nan,nan,[0.85 0.85 0.85], ...
    EdgeColor='none', ...
    FaceAlpha=0.5);

obj.h_thrline = line(obj.plotAxes_, nan, nan, ...
    'Color', [0.25 0.25 0.25], ...
    'LineWidth', 2, ...
    'LineStyle', '-', ...
    'Marker', 'none');

obj.h_line = line(obj.plotAxes_, nan, nan, ...
    'Color', obj.LineColor, ...
    'LineWidth', 1.5, ...
    'LineStyle', '--', ...
    'Marker', 'none');

obj.ReversalUpH = scatter(obj.plotAxes_, nan, nan, obj.ReversalMarkerSize, ...
    'Marker', '^', ...
    'MarkerEdgeColor', obj.ReversalColor, ...
    'MarkerFaceColor', obj.ReversalColor, ...
    'LineWidth', 1, ...
    'Visible', 'off');

obj.ReversalDownH = scatter(obj.plotAxes_, nan, nan, obj.ReversalMarkerSize, ...
    'Marker', 'v', ...
    'MarkerEdgeColor', obj.ReversalColor, ...
    'MarkerFaceColor', obj.ReversalColor, ...
    'LineWidth', 1, ...
    'Visible', 'off');

obj.h_points = scatter(obj.plotAxes_, nan, nan, obj.MarkerSize, ...
    'filled', ...
    'Marker', 'o', ...
    'MarkerEdgeColor', 'none');

obj.StepH = scatter(obj.plotAxes_, nan, nan, obj.StepMarkerSize, ...
    'filled', ...
    'Marker', 's', ...
    'MarkerEdgeColor', 'none', ...
    'Visible', 'off');

hold(obj.plotAxes_,'off')

obj.createPlotContextMenu_();
